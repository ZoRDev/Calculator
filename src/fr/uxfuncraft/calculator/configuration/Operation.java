package fr.uxfuncraft.calculator.configuration;

public enum Operation {
	/**
	 * The operation must be an addition
	 */
	Addition,
	
	/**
	 * The operation must be a subtraction
	 */
	Subtraction,
	
	/**
	 * The operation must be a multiplication
	 */
	Multiplication,
	
	/**
	 * The operation must be a division
	 */
	Division,
	
	/**
	 * The operation must be a modulo
	 */
	Modulo,
	
	/**
	 * The operation must be a cosine
	 */
	Cosine,
	
	/**
	 * The operation must be a sine
	 */
	Sine,
	
	/**
	 * The operation must be a tangent
	 */
	Tangent,
	
	/**
	 * The operation must be a root
	 */
	Root,
	
	/**
	 * The operation must be a power
	 */
	Power;
}
